export const THEMES: Theme[] = [
  TOKYO_NIGHT,
  DRACULA,
  NORD,
  CATPPUCCIN_MOCHA,
  ONE_DARK,
  GRUVBOX,
  MONOCHROME,
  GITHUB_LIGHT,
  CLAUDE,
  CLAUDE_DARK,
  CYBERPUNK,
  MATRIX,
  AMBER_PHOSPHOR,
]

const DEFAULT_THEME_ID = 'tokyo-night'

export function findTheme(id: string): Theme {
  return THEMES.find(t => t.id === id) || TOKYO_NIGHT
}

export function applyTheme(theme: Theme) {
  const root = document.documentElement
  for (const [key, value] of Object.entries(theme.variables)) {
    root.style.setProperty(`--${key}`, value)
  }
  const meta = document.querySelector('meta[name="theme-color"]')
  if (meta) meta.setAttribute('content', theme.variables.background)
}

export function loadAndApplyTheme() {
  try {
    const raw = localStorage.getItem('control-panel-prefs')
    if (raw) {
      const prefs = JSON.parse(raw)
      if (prefs.theme && prefs.theme !== DEFAULT_THEME_ID) {
        applyTheme(findTheme(prefs.theme))
      }
    }
  } catch {}
}
